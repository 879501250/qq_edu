<p>&ldquo;计算&rdquo;当然是&ldquo;数学&rdquo;，但远远不能等同于&ldquo;数学&rdquo;。数学是一个庞大的知识体系，它包括了代数、几何、概率、集合、拓扑以及逻辑推理（数学思维类）等诸多分支，而&ldquo;计算&rdquo;仅仅是代数分支中一个非常微小的知识点而已。<strong>从启蒙角度来讲，我们需要呈现给孩子的是一个&ldquo;更为全面、更为本质&rdquo;的数学，而不能用简单的数数和运算来代替数学。</strong></p>
