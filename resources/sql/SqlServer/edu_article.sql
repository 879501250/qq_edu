<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">幼儿数学认知发展</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">孩子的数学能力不会凭空跳跃，而是随着年龄逐渐发展成熟。父母要先掌握儿童数学发展概况，才能协助孩子累积数学经验，发挥数学潜能。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">孩子的数学概念发展包括了4大领域：数、量、图形空间、逻辑关系。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">然而，我们一定要知道，就跟每个孩子的身高、体重发展各有不同一样，孩子的数学发展也有个别差异，当然父母的引导方式和态度也会影响孩子的数学能力。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics4.baidu.com/feed/7af40ad162d9f2d3aa88b0d38424d1166227ccbb.jpeg?token=0dea1a75bdf86f477b3b0fe8a3a2be38&amp;s=F994499D5E3354840D9870F203008032" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">0-2岁</span></span></span><span class="bjh-p">1、数：可以从1背到10</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">1岁左右的婴幼儿，已有&ldquo;更多&rdquo;的概念，例如当大人给的饼干不如预期时，会表示&ldquo;还要&rdquo;。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">快2岁时，会开始学数数，可以从1数到10，甚至更多，但通常只是单纯的数字背诵，并不了解数字的意义。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">有些2岁的孩子会开始听得懂&ldquo;拿1个&rdquo;或&ldquo;选2个&rdquo;这种简单的指令。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2、量：开始探索&ldquo;大小&rdquo;和&ldquo;轻重&rdquo;</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">快1岁的孩子，已经能笼统地感觉出物品的大小或轻重，以及物品和自身的距离；1-2岁时，喜欢玩水、玩沙，用不同的容器探索量的多少。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics3.baidu.com/feed/c995d143ad4bd113325ba7a77667ff0a49fb05d9.jpeg?token=9f75da11150e05ba1527900b0e389a49&amp;s=7B429B1E062171091E68586D0300E070" width="600" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3、图形空间：堆积木、认图形</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">1-2岁时，能把相同大小的图形做配对；随着经验的累积，孩子慢慢学会将不同大小但同样形状的图形做链接，例如：小圆形和大圆形、小三角形和大三角形。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2岁的孩子，通常喜欢把积木堆叠高高的，也能拼出图像简单的拼图；此外，还开始会留意生活周围的图案，例如：地板磁砖上的特殊形状。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4、逻辑关系：了解规律生活</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">快1岁的婴幼儿，开始了解生活的简单规律，例如：洗完澡、刷完牙后，就要上床睡觉了。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">1岁半的孩子会开始寻找事物的规律和逻辑顺序，例如：白天和黑夜会轮流交替。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics1.baidu.com/feed/c9fcc3cec3fdfc03980eeca2f9f7dc91a5c22686.jpeg?token=179de85e1f6cea48d069b5b1b6a499ae&amp;s=3D84DB154E44794300BDD1DE03005021" width="626" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">2-4岁</span></span></span><span class="bjh-p">1、数：掌握数字和实物的关联</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2岁半到3岁的孩子，开始会数物品的个数。3岁的孩子可以清楚告诉大人自己的年纪，并且用手指比出3。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">这个年纪的孩子已经建立了数字和物品间的关联，可依指令排出和数字相当的物品总数，例如：2个苹果、5个杯子。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2、量：可区分大小高矮胖瘦</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2岁半到3岁时，可清楚区分出物品特征，例如：大小、高矮、胖瘦、长短、轻重、粗细。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3岁半的孩子具有明确的度量观念，例如：当瓶中的水倒出一半，量就会减少；相反的，如果再多加水进去，他们也知道水量会增加。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics4.baidu.com/feed/37d3d539b6003af33d9c244d18e29d591138b6c2.jpeg?token=4c7fab67c6ba7041fc86ef9aa5333fe9&amp;s=ABA3CF0452815557C69CAA670300F0E7" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3、图形空间：用身体探索空间</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2-4岁的孩子开始学习用身体去探索空间，体会自己的上下、左右、前后等空间关联。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4岁的孩子可以清楚辨別并说出不同大小的各种形状；甚至，可以用积木堆叠出左右对应的架构。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4、逻辑关系：练习各种分类方式</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3岁时，孩子开始有清楚的分类能力，例如：按照物品的颜色、形状、大小、材质或用途做分类。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4岁的孩子可以做较复杂的序列游戏，例如：将物品从大排到小，并清楚说出&ldquo;比较大&rdquo;、&ldquo;最大&rdquo;等词。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics3.baidu.com/feed/d4628535e5dde711ab86b35e8927951e9c16610b.jpeg?token=ca46fa37e1c41fbd7e63379e5d685e3b&amp;s=D5024BBE1A320782872D3D640300F060" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">4-6岁</span></span></span><span class="bjh-p">1、数：会做简单运算</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">5-6岁时，能以图像化方式进行答案是10以内的简单加法与减法。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">6岁时，孩子开始能理解&ldquo;数字守恒&rdquo;的道理，也就是说物品的数量不会随着外在形状而改变，但仅限于10以内的数量。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2、量：喜欢比一比</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4-5岁的孩子喜欢借由&ldquo;比较&rdquo;来解决问题，例如：比比哪支筷子比较长？也能针对同样形体但不同大小的容器，辨別出哪个比较大、哪个比较小？</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">这时候的孩子嘴上喜欢用&ldquo;比较&rdquo;和&ldquo;最&rdquo;的字眼，例如：我比弟弟还要高、我和妹妹一样高、哥哥最高等等。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics4.baidu.com/feed/5ab5c9ea15ce36d3afa9e12f173b6182e850b149.jpeg?token=64edd470aa139c3fdd979c27322bbbdf&amp;s=0033C530475643C67CE124F503001022" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3、图形空间：照着图案堆积木</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">5岁的孩子开始能模仿书本里的图像，甚至开始能画出记忆中的图像。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">6岁时，他们能运用2个图形排出另1个图形，例如：用2个三角形排出1个正方形、用2个正方形积木排出1个长方形。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4、逻辑关系：清楚做事顺序</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4-5岁的孩子能懂得按照物品的特性做精细的分类，例如：把袜子按照颜色区分开来，再依照大小排好。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">6岁时，能清楚明白事件发生的前后顺序，这也是认识时刻的基础知识，例如：洗澡前要先刷洗浴缸，接着打开水龙头把水加满浴缸，最后才能开始洗澡。</span></p>
</div>
