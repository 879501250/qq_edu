<p><strong>看到这儿你是不是已经开始焦虑起来，幼儿园的数学竟然如此广阔浩瀚！！！我的孩子还在整天玩小汽车，这可怎么办？</strong></p>
<p>其实，大可不必&ldquo;焦虑&rdquo;，无论是&ldquo;国标&rdquo;还是&ldquo;美标&rdquo;，所涉及的数学知识其实都非常有限，无非就是<strong>数与计算、形状与空间、逻辑与推理</strong>。这些知识，一般的成年人都是熟练掌握的，只不过我们不熟悉这些专业名词而已。（PS：如果你还是觉得焦虑，那就赶紧关注单爱公众号吧，博士爸爸会持续推送数学启蒙的文章，帮你消除焦虑^_^）</p>
<p>通过查阅这些权威文件，我给&ldquo;数学启蒙&rdquo;下了一个定义：所谓&ldquo;数学启蒙&rdquo;，其核心在于&ldquo;启蒙&rdquo;，而不是具体的&ldquo;数学知识&rdquo;。</p>
<p>数学启蒙最重要的任务是让孩子对数学有一个好的印象，通过认知、感知数学的有用和有趣，进而喜欢上数学。至于孩子在学龄前能学到多少数学知识，则不必过于关注，能学多少就学多少，顺其自然即可。</p>
<p>学龄前所涉及到的知识都非常简单，随着孩子年龄的增长，他们都是可以轻松学会的，比如从1数到100，从100数到1000，或者简单的加减法，对于3岁的孩子可能有些困难，对于4岁的孩子来说就变得简单很多了。</p>
<p>但是，能否让孩子对数学感兴趣，就不是一件随着年龄增长而能够解决的事情了。为了避免给孩子的数学学习&ldquo;帮倒忙&rdquo;，我总结了启蒙时期的&ldquo;三不原则&rdquo;：</p>
<p><strong>原则一：不要因为孩子没有学会你所教的数学知识而去批评他或打击他；</strong></p>
<p><strong>原则二：不要把孩子掌握数学知识的多少作为数学启蒙成果的衡量标准；</strong></p>
<p><strong>原则三：不要拿孩子掌握的数学知识多少来与其他孩子进行比较或者炫耀。</strong></p>
<p>上述这些行为都会让孩子对数学产生厌恶的情绪，这不仅不能让孩子对数学有一个好印象，而且还会破坏孩子对数学的兴趣，甚至影响孩子上小学后学习数学的信心。如果是这样，那我们的启蒙行为就是在给孩子&ldquo;帮倒忙&rdquo;了。</p>
<p><strong>02 为什么需要数学启蒙？</strong></p>
<p>关于&ldquo;为什么需要数学启蒙&rdquo;的问题，我觉得需要分两个方面来讨论，一方面是关于孩子的数学能力，一方面是关于孩子的升学能力。</p>
<p>数学启蒙属于早期教育的范畴，而关于早期教育有没有用，其实是一个争议很大的话题，网上也有很多表达不同观点的文章。在这里，我们先来了解两个最具代表性的科学研究。</p>
<p>第一个科学研究来自于<a class="internal" href="https://www.zhihu.com/search?q=%E4%B8%87%E7%BB%B4%E9%92%A2&amp;search_source=Entity&amp;hybrid_search_source=Entity&amp;hybrid_search_extra=%7B%22sourceType%22%3A%22answer%22%2C%22sourceId%22%3A1254613874%7D" data-za-detail-view-id="1043">万维钢</a>老师的文章《科学结论：早教对孩子不仅没用，反而有害》。（PS：万老师是我在科普写作方面的偶像，同时也是我中科大物理学院的师兄。）</p>
<p>在万老师的文章中提到，德国政府曾资助过一项研究，研究者选择了50个以早教为特色的幼儿园和50个以玩闹为主的传统幼儿园，对所有这些孩子进行跟踪比较。结果发现，一开始早教幼儿园出来的孩子学习水平确实更高，而且这个优势一直保持到孩子们上小学以后。</p>
<p>但是，随着年龄的增长这个优势逐渐下降，到小学四年级，早教组的孩子不但没有学习优势，而且他们的成绩还显著低于传统幼儿园出来的孩子。所以，万老师的观点是，早教确实能让孩子赢起跑线，但是领先不了多久。</p>
<p>第二个科学研究来自陈鹤琴老先生的代表作之一《儿童心理》。在陈老先生的书中提到，1929年美国心理学家格赛尔（Gesell）的双生子实验。格赛尔选择了一对同卵双胞胎女孩A和B，在她们出生第48周时，格赛尔对女孩A进行了爬楼梯、搭积木、肌肉协调和运用词汇等训练，而对女孩B则不作训练。</p>
<p>在第53周时，格赛尔把女孩A和女孩B放到相同的环境中测试爬梯技能。结果显示，接受训练的女孩T在爬梯这项技能上明显优于女孩B，两个人的爬梯时间分别是17秒和47秒，女孩B比女孩A落后了30秒。之后，格赛尔让女孩B也开始接受相同的训练。</p>
<p>结果显示，只需要少量的训练，女孩B的爬梯技能就赶上了女孩A的技能。到了第55周，两个女孩的爬梯技能已基本看不出差别。</p>
<p>赛尔的实验表明，<strong>在身体成熟之前，人为外加的&ldquo;学习&rdquo;作用非常小，而在身体成熟之后进行专项训练效果会特别明显</strong>。这也是陈鹤琴老先生关于早教的观点。</p>
<p>结合上述两个研究成果，我对于&ldquo;数学启蒙是否有用&rdquo;的回答是：</p>
<p><strong>① 在孩子身体发育成熟的时候（主要是指大脑思维的发育程度），给予适当的数学启蒙教育，对孩子的数学能力是有明显提升作用的；</strong></p>
<p><strong>② 想要孩子一直保持住数学能力的优势，启蒙教育的工作不能仅仅停留在幼儿园阶段，而是要贯穿整个小学阶段，一直到孩子掌握了自主学习能力为止。</strong></p>
<p>简单地说，就是：<strong>数学启蒙是有用的，但需要结合孩子的成长节奏来开展，而且数学启蒙不能只停留在幼儿园阶段，而是要一直贯穿整个小学阶段。</strong></p>
<p>看完两个烧脑的学术知识，我们来聊一聊不烧脑但很烧心的问题：升学能力。</p>
<p>这是网上流传的两道&ldquo;幼升小&rdquo;考试题。虽然无法判断真假，但当下中国孩子的升学问题依然是摆在各个家庭面前的头等大事。</p>
<p>中国的教育制度属于选拔制（也可以称为淘汰制）的教育，想要在这样的教育体制中更好地生存，就必须认真对待每一次淘汰测试，否则吃亏的只能是自己。而数学，则是这些淘汰测试中的重头戏。</p>
<p>带着这样的目的学数学，当然不是一个好的教育导向。但是，不管我们推崇多么先进的教育理念，首先还是需要带领孩子脚踏实地地面对现实。我们需要做的，就是尽量在不破坏孩子天性的前提下，帮助孩子去做好准备迎接每一次的淘汰测试。</p>
<p>所以，从升学的角度讲，数学启蒙本身就是一件需要我们认真去对待的事。</p>
<p><strong>03 数学启蒙怎么做？</strong></p>
<p>好了，终于来到大家最关心的部分，数学启蒙怎么做？</p>
<p>结合前面的理论分析和我自己的实践经验，我总结了一个&ldquo;三步走&ldquo;&rdquo;的数学启蒙方法，在这里和大家分享。</p>
<p><strong>第一步：观察，判断孩子的数学思维处于哪个状态</strong></p>
<p>可能很多人会奇怪，第一步怎么是观察呢？我的依据是格赛尔的双生子实验，也就是要等待身体成熟后再给予适当的训练。</p>
<p>数学启蒙是一项思维活动，只有在孩子的大脑发育到能够接受这类知识的时候，教给他适当的数学知识才是有效的。如果启蒙太早，孩子难以接受，学习效率会比较低下。如果父母比较着急，一定要让孩子提前于大脑发育进行数学训练，当然也可以让孩子提前掌握相关数学知识，但这种做法一般会事倍功半，甚至还会破坏孩子对数学的兴趣，从长远来看是不推荐的。如果启蒙太晚了，错过最佳练习阶段，也就错误了最好的启蒙时间了。</p>
<p>所以，数学启蒙的第一步是要观察孩子数学思维处于哪个状态，从而判断他适合学习哪些数学知识。</p>
<p>以数数为例，Shen从一岁多就可以很完整地从1数到10，但这并不代表他已经掌握了数数的能力。我是通过一些简单的测试来判断的，比如有3个人，他数到3之后还会继续数下去，这就说明他还不明白数数的概念。</p>
<p>按照正常规律，这个时候他的大脑还没发育到能够理解抽象符号的时候，他只不过是凭记忆力在数数而已。这个时期，数数对于他来说，和哼唱一首儿歌并没有什么差别。</p>
<p>到了2岁左右，Shen开始能够理解1，2，3的意义。因为这个时候，我问题他屋里有几个人，他可以很准确地数出是3个人，或4个人，但偶尔还是会数错。这个时候，我认为有必要帮助他开始&ldquo;数数&rdquo;的学习了。</p>
<p>到了2岁半，Shen已经可以很准确地从1数到10，我更换不同的物品，比如手指，人，椅子，鞋子，他都可以很准确地输出来。这就说明他头脑里已经具备了简单数数的能力。这个时候，就可以开始进行一下复杂的数数的练习，比如从10数到20，比如从杂乱的积木中数出几个三角形，或者从一堆袜子中数出红色的袜子等等。</p>
<p>先观察，先试探，确认孩子具有学习某一知识点的生理结构（大脑）后再进行引导学习。对于形状和空间，逻辑推理等，也都可以通过同样的方法来判断是否可以进行知识学习。</p>
<p><strong>第二步：重复，在日常生活中不断重复近期学习的知识点</strong></p>
<p>还是以数数为例，在我确认Shen已经具备数数能力之后，我就带他下飞行棋。飞行棋是个非常好的练习数数的场景。刚开始，Shen要对着骰子进行&ldquo;指数&rdquo;，慢慢地他开始不需要手指了，而是在心里&ldquo;默数&rdquo;。大概到了三岁，他就可以一眼就可以看懂数字了。这个时候，已经说明他很好地理解抽象的符号数字与具体物品的量之间的关系了。</p>
<p>除了飞行棋，还可以通过数玩具车，数积木，数楼梯，数步数等等各式各样的生活和游戏来练习数数。</p>
