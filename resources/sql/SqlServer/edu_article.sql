<p class="ztext-empty-paragraph">&nbsp;</p>
<p><strong>C - Concrete，也就是具象化</strong></p>
<p>具象化主要就是使用<strong>数学教具</strong>，这块主要用来培养孩子的数感。</p>
<p>这就是之前说的1个橘子+2个橘子=3个橘子的过程。这个物化的过程，让运算有可触摸可参照的对象，孩子很容易理解。但是这样的具象化学习是有局限的，只能做基础的加减运算，稍稍复杂的乘除，用实物解释起来就不够方便了。</p>
<p><strong>P - Pictorial 也就是，形象化</strong></p>
<p>形象化，是通过图像符号，将问题表述出来，是为由形象向抽象过渡做准备。新加坡的数学基础教育的最大特点，就是强化了&ldquo;P-形象化&rdquo;这个过程，这个过程就是绘画建模（Model Drawing）。注重结果同时注重过程，给孩子不一样的解题思路。</p>
