<p>新加坡的数学教育在注重结果的同时，也非常在意&ldquo;过程&rdquo;二字。小朋友学习的时候，很清楚地知道，<strong>必须要写过程，必须要把他们的解题方法和思路表达出来</strong>，一旦自然而然形成了这样的数学思维，解题方法就不是问题，解题的结果也是显而易见的，而且学得更好一点的小朋友还能运用于生活。</p>
