<p><span class="bjh-p"><span class="bjh-strong">1. 培养孩子对事物感兴趣及喜爱的能力</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">大脑在接收信息后，会自动把这个信息贴上一个标签，喜欢或不喜欢。<span class="bjh-strong">对于不喜欢的事物，大脑的理解、思考和记忆等功能</span><span class="bjh-strong">就无法完全运作</span><span class="bjh-strong">。</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">因此要<span class="bjh-strong">充分刺激大脑</span>，就必须<span class="bjh-strong">引导孩子对新鲜事物保持浓厚兴趣，喜欢尝试</span>。大脑也就会发挥最大的运作能力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t11.baidu.com/it/u=735222700,3386763673&amp;fm=173&amp;app=25&amp;f=JPEG?w=640&amp;h=427&amp;s=C942DF18030067530B40BDFF0300F020" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">从脑医学的角度上来说，对什么事物都提不起兴趣的孩子，无论在运动或学习上，都无法充分发挥应有的实力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">2. 训练孩子学会聆听</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">神经群中掌管感情的一部份，在情绪活动下会<span class="bjh-strong">提高判断力与理解力</span>。当听到别人的话或接触新事物有所触动时，就能充分发挥大脑的能力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">相反，如果凡事都不屑一顾，这种态度对大脑的发展来说非常不好。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">培养孩子养成对任何事情都认真聆听的习惯，可以告诉孩子&ldquo;可能会听到有趣的事情哦&rdquo;、&ldquo;可能会学到我们不知道的新知识呢&rdquo;。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t11.baidu.com/it/u=570270589,1202134716&amp;fm=173&amp;app=25&amp;f=JPEG?w=639&amp;h=426&amp;s=7184FD1F43B077920AA8D1FF03005063" width="639" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">对听到的事情，家长可以带头表示&ldquo;好棒哦&rdquo;、&ldquo;很有趣呢&rdquo;，培养孩子感动的能力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">3. 培养孩子全力以赴的个性</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">很多人会觉得，孩子还这么小，做事情何必这么认真呢？<span class="bjh-strong">这样的态度很容易让大脑发展停滞。</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">培养孩子对任何事情，全力以赴的态度，能最大程度让大脑发挥功能。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">其实就算是每天晚上，要求孩子自己收拾好玩具这种小事，也可以达到这种功效，比如一开始只要收好，到后来慢慢要求分类，都是逐渐设定目标，然后让孩子全力以赴完成。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t11.baidu.com/it/u=853560535,1582568358&amp;fm=173&amp;app=25&amp;f=JPEG?w=640&amp;h=426&amp;s=7CBA2FD7C2317D984A364CE40300703A" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">要有不管面对多大、多小的事情，都能不偷懒、全力以赴的态度，才能将大脑的能力发挥得淋漓尽致。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">否则没有主动去做、并要设法完成的态度，如果一遇到困难，家长就立刻插手给予帮助，大脑就无法充分表现能力。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t11.baidu.com/it/u=1553129740,2712390752&amp;fm=173&amp;app=25&amp;f=JPEG?w=640&amp;h=106" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">6招教你</span><span class="bjh-strong">成为宝宝大脑发育的有力支援</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">1. 充分给予关爱</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">孩子在0-3岁时如果没有获得足够的疼爱，就无法均衡发展生存本能，大脑为了保护自己，会让保护的本能反应过度。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">很多人认为宝宝哭泣时，不可以太宠，放任孩子哭一会而就停了，其实这是不正确的。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">对于还不会说话的宝宝而言，<span class="bjh-strong">&ldquo;哭泣&rdquo;是和爸妈建立沟通的方式，如果爸妈不理会，宝宝就感受不到爸妈的关爱</span>。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t10.baidu.com/it/u=622979445,1079760335&amp;fm=173&amp;app=25&amp;f=JPEG?w=640&amp;h=427&amp;s=7DDA6F9E1C72719E81B954620300B071" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">因此，宝宝一旦哭了，一定要抱孩子并柔声安抚，不能让宝宝觉得很孤单、没人疼的感觉。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">所以爸妈要确实将自己的爱传达给孩子，要不断和宝宝有肌肤接触或说话，并以开朗的表情对小宝宝，尽可能多露出笑容。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">2. 不说否定字眼</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">面对困难、辛苦的事情时，人常会脱口而出&ldquo;没办法吧&rdquo;、&ldquo;办不到&rdquo;，这类否定的话，然后大脑就会对这件事情，贴上一个负面标签，于是思考力与记忆力都会自动降低。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">经常鼓励孩子、对孩子认真的付出表示赞美，</span>不让孩子陷入负面思考。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t10.baidu.com/it/u=332679927,1342799829&amp;fm=173&amp;app=25&amp;f=JPEG?w=628&amp;h=314&amp;s=BDAAFF16CC635103026CF5FD03004063" width="628" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">当孩子主动面对难题时，爸妈在一旁可以帮忙先整理什么是孩子做得到的、哪些是孩子可能做不到的，确定目标后，陪着孩子，一起想怎么克服困难去完成，这就是激发大脑实力最好的练习。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">3. 常常称赞</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">对于0-3岁的孩子，父母要尽量赞美。就算出了一些状况，失败了，也要鼓励&ldquo;没关系，再来一次，一定可以做到哦&rdquo;这样，孩子的脑神经细胞会充满活力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">相反，要是孩子在小时候经常被骂，大脑中自我保护的本能很容易反应过度。孩子会心想：&ldquo;我一失败就惹大人生气，那我就尽量少做、少失败&rdquo;。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">于是失去了面对新挑战的精神，或是想办法隐藏失败，变成不肯承认过错的小孩。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">4. 不要阻止孩子在做的事情</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">0-3岁的婴幼儿大脑开始觉得感兴趣是件好事、喜欢一件事的感觉真好，因此千万不要阻止孩子接触感兴趣与关心的事物。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">当孩子因为有兴趣而伸出手时，大人是不是常会说&ldquo;不行&rdquo;然后把东西拿走，如果多次碰上这种状况的话，孩子可能认为新事物是不被大人认可的，会排斥新事物。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">要充分培养孩子求知的本能，<span class="bjh-strong">就该让0-3岁的孩子尽量自由做任何事</span>；父母该做的是把真正会造成危险的东西收好，或是在旁陪伴注意，看护好孩子，在父母设定安全的环境下，就让孩子尽可能自己探索环境，培养孩子的求知欲。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">5. 用孩子的角度跟孩子一起游戏</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">要培养孩子的大脑、锻炼脑神经细胞本能，最有效的方法就是一起玩。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://t10.baidu.com/it/u=1449500799,902497651&amp;fm=173&amp;app=25&amp;f=JPEG?w=639&amp;h=402&amp;s=3048FA155A60729C0519C4EA0300C023" width="639" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">父母一定要把自己当成孩子，认真地玩游戏。如果家长开始觉得无趣、厌烦，孩子会敏锐地察觉，所以务必要集中精神，观察自己的孩子对哪些事有兴趣。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">6. 随时保持愉快地沟通</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">就算在孩子还不懂得语言的阶段，也可以让他听到开朗的语气和笑声，露出笑容，达到沟通的目的。充满关爱地凝视孩子双眼、与他交谈，就能培养他日后积极与人沟通的能力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">0-3岁孩子与父母沟通的方式是锻炼他日后跟同伴沟通方式的关键，记得随时要与孩子保持愉快的沟通。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p"><span class="bjh-strong">最后给大家划个重点</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">1.&nbsp;<span class="bjh-strong">负面环境因素</span>（包括：受虐,忽略不理等）<span class="bjh-strong">会对大脑发育产生负面影响</span>。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">2. 虽然脑部具有终生可塑性，但大脑发育的确存在&ldquo;敏感期&rdquo;，<span class="bjh-strong">婴幼儿期就是重要敏感期。</span></span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">3.&nbsp;<span class="bjh-strong">充足的营养是脑部发育必需的</span>，缺乏某些元素会使脑部发育不良，如铁、脂肪等。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">4. &ldquo;人&rdquo;的刺激优于&ldquo;物&rdquo;的刺激，所以家人互动一定会比电视、电子产品、或玩具好。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">5. 有来有往、一来一去的<span class="bjh-strong">游戏互</span><span class="bjh-strong">动</span>，比任何昂贵的玩具更好。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">6. 孩子的大脑很早在三个月大时，就可以辨识父母的情绪，温暖平静比紧张焦虑好。忧郁、焦虑等负向情绪有负面影响。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">7. 花时间<span class="bjh-strong">了解每一个孩子的独特性</span>，比到处找专家好。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">环境的刺激是小朋友成长过程中不可或缺的要素。父母亲的陪伴，简单的呵护拥抱、安抚、说话、唱歌，并加入触摸积木、认识彩色物体的活动，都能对孩子的反应接收、感觉统合产生刺激，有助于未来在学校的表现，包括和同学老师的相处，学习能力。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p>&nbsp;</p>
</div>
