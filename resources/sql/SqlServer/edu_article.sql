<p>在Shen具备数数能力之后，我曾尝试拿两个骰子，引导他学习加法。但他没有接受这种玩法，说明他还不具备学习加法的能力。这个时候我也就不强制他学习加法。转为第一步，观察，观察并测试他什么时候具备了学习计算的思考能力。</p>
<p><strong>第三步：课程，适当地引入系统性学习</strong></p>
<p>在Shen接触到了一定的数学知识后，我就带他一起读数学启蒙的读物，开始尝试进行系统性的学习模式。数学启蒙读物的前几讲是关于数字、数量的内容，这对Shen来说已经很容易的事情，因此也就引起他对这套书的学习兴趣。到目前为止，他一知道我要带他学数学，就会非常兴奋。这也是出乎我自己的意料的。</p>
<p>不过，为了让他保持对数学的持续兴趣，我和他一起立了一个规定：一次课只能讲一节内容。这样的约定有两个考虑，一方面一个三岁的孩子能够真正理解的知识是非常有限的，一次给他讲太多知识，他吸收不了，反而就削弱了对数学的兴趣；另一方面，控制每次学习的量，也可以起到&ldquo;饥饿营销&rdquo;的效果，这样可以帮助孩子保持住对数学知识的渴望。</p>
