<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">设计数字的游戏：</span></span></span><span class="bjh-p">没有什么比从&ldquo;玩中学&rdquo;更适合孩子、效果更好的了。父母可以借由游戏的方式，加强孩子的数字概念：</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">跳格子：适合3岁以上的孩子</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">利用组合拼垫或粉笔，在地面上画出排列数字的方格，参与者投掷小石块到格子中，再经由跳格子的方式，取回小石块得一分。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">这个游戏是希望借着跳格子，让孩子在游戏中，熟悉数字的连贯性，格子可以任意重新排列组合但要维持顺序。</span></p>
