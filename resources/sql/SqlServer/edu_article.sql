<p>不过，我自己感觉这个要求是偏低的，大部分4岁左右就可以达到5~6岁的水平。于是，我自己也总结了一些&ldquo;图形认知&rdquo;的几个内容，和大家一起分享。</p>
<p>我自己在实践过程中，把图形认知总结为4个方面的内容：</p>
<ul>
<li>图形识别</li>
<li>图形组合与分解</li>
<li>图形旋转与移动</li>
<li>图形延伸</li>
</ul>
<p><strong>&middot; 图形识别</strong>，顾名思义就是认识图形，包括三角形、圆形、正方形等。随着孩子年龄的增长，我们可以让孩子进一步接触一些复杂图形，比如六边形、八边形、三棱柱、六棱柱、三棱锥等。<strong>&middot; 图形组合与分解</strong>，简单地说就是用几个图形拼成另外一个图形，或者把一个图形拆成几个图形。<strong>&middot; 图形旋转与移动</strong>，就是把图形旋转起来，变成其他图形，比如长方形沿着一条边旋转起来就变成圆柱体，一个三角形旋转起来变成圆锥体之类的。移动也是一种变化，比如一个正方形移动起来可以变成正方体或长方体。<strong>&middot; 图形延伸，</strong>就是看到一个不完整的图形或残缺的图形，可以想象出完整图形的样子。或对着一个图形，想象出它外延的样子。最典型的例子就是拼图。</p>
