<p class="ztext-empty-paragraph">&nbsp;</p>
<p>在之前那篇关于新加坡教育的文章里我提过，<strong>建模思维是新加坡数学最重要的特色</strong><strong>。</strong>通过建模，可以将复杂的数学问题简化成形象直观的计算方式，让孩子更容易地理解。</p>
<p class="ztext-empty-paragraph">&nbsp;</p>
<p>&nbsp;</p>
<figure data-size="normal"><img class="origin_image zh-lightbox-thumb lazy" src="https://pic1.zhimg.com/80/v2-8fb14c38ebdf04df385327f4d880e178_720w.jpg" width="856" data-rawwidth="856" data-caption="" data-size="normal" data-rawheight="480" data-original="https://pic1.zhimg.com/v2-8fb14c38ebdf04df385327f4d880e178_r.jpg" data-actualsrc="https://pic1.zhimg.com/v2-8fb14c38ebdf04df385327f4d880e178_b.jpg" data-lazy-status="ok" /></figure>
