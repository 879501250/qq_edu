<p>刚开始接触到这些视图的时候，我感觉到非常吃力。当时觉得，那些工厂的师傅光看这些图就能把零部件加工出来，真的是难以想象。对于我来说，想要把这些视图准确无误地画下来，几乎是不可能的事情。所以，每次作业我都要花很多的时间。但是，我们班里就有一些空间感很强的同学，他们看一下实物，然后就可以像拍照一样把这些视图非常快地画出来，而且基本不会出错。当时，我是真真切切地感受到了什么叫做空间感的差距。好了，这就是我跟大家分享的故事，这个事情让我形象地感受到了空间感的存在，而且空间感是可以通过培养来提升的。</p>
<p>那么，空间感究竟是什么呢？其实，空间感的范畴是非常广的，从幼儿园的图形认知到大学里的机械制图，甚至到最前沿的物理学、天文学、宇宙学的研究内容，都属于空间感的范畴。按照定义来说，<strong>空间感就是人对物体结构形状、空间位置、远近层次和距离的感觉。它的底层逻辑，实际上就是一个人的想象力和推演力。</strong></p>
<p class="ztext-empty-paragraph">&nbsp;</p>
<p>2<strong> 为什么需要培养&ldquo;空间感&rdquo;？</strong></p>
<p>那么，我们为什么需要这么早就重视孩子空间感的建立呢？这是因为，有了良好的空间感的人，确实能带来很多的好处。</p>
<p><strong>首先，空间感强的人绘画/制图能力都会比较强。</strong>无论是艺术类的绘画，还是机械类的制图，或者是建筑类的素描图，拥有良好的空间感是最基本的要求，这个应该不用做过多的解释。<strong>其次，空间感强的人方向感一般都会比较强。</strong>对空间感强的人，他们可以很轻松就在头脑中构建一个虚拟的空间，简单的就是按东西南北或者前后左右构建。在这个虚拟的空间里，只要有人跟他说往北走，他可以完全不用看地图，通过头脑里虚拟的空间就可以找到想要的方向。现在很多网络游戏，对空间感的要求也是很高的，想要玩得好，也是要提升空间感的。<strong>最后，空间感强的人还有一个很重要的能力，那就是推演能力。</strong>空间感的本质是在头脑里构建出立体图像的能力，这种能力也可以用来在头脑里推演某个事情的动态发展，或某个现象的动态演变。爱因斯坦就是一个空间感超强的人，他的相对论很大部分是靠头脑里的&ldquo;思想实验&rdquo;推演出来的。我们古代有&ldquo;运筹帷幄&rdquo;的说法，指的也是这种推演能力。</p>
