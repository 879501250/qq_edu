<p class="ztext-empty-paragraph">&nbsp;</p>
<p><strong>学习和掌握"数的进制"对孩子建立良好数感是有很大帮助，这可以让孩子感觉到自己和其他人相比，跟数字走得更近，关系更亲密。</strong></p>
<p>学龄前的小孩当然不用学习二进制，但需要让他们理解十进制的概念，再通过一定量的练习让孩子熟练掌握十进制的方法。比如：很多孩子通过背诵数字知道59之后是60，并不是真正掌握十进制的方法，我们需要让孩子明白之所以59之后是60，是因为9之后就要往前进一位，所以变成6和0。如果孩子理解了，那就可以很轻松地一直无限地数下去了。</p>
<p><strong>数的量级</strong></p>
<p>我曾在网上看到过一个帖子，一个小学老师让学生自编应用题，结果出现一些让人哭笑不得的笑话，比如&ldquo;门高2厘米&rdquo;、&ldquo;妈妈的体重100克&rdquo;、&ldquo;小明每秒钟走路1000米&rdquo;、&ldquo;小红的爸爸今年12岁&rdquo;&hellip;&hellip;</p>
<p><strong>在这些哭笑不得的笑话背后，其实折射出的是我们现行数学教育背后一个大问题&mdash;&mdash;数学与生活完全脱轨</strong>。其实不止小学生，即便是大学生，在数的量级上也常常会犯低级错误。</p>
<p>数的量级是一个很实用的概念，在工程领域，大部分情况下是不需要进行精确计算的，而是通过量级估算来进行宏观判断。一位高水平的工程师可通过量级估算轻松找出很多工程设计上的低级错误。</p>
<p><strong>对于学龄前儿童来说，学习数的量级最简单的办法就是用实物来对比。</strong>通过不同数量的实物来对比，可以直接给孩子留下不同量级的感官印象。</p>
<p>比如，我们可以用一个瓜子代表1，一个小金桔代表10，一个苹果代表100，一个西瓜代表1000，通过这样的对比来让孩子建立1和100，1和1000的量级差别。</p>
<p>再比如，我们可以告诉孩子从家门口走路到电梯口是10米，从家里走路到幼儿园是100米，从家里走路到XX餐厅是1000米。通过类似的对比来帮助孩子建立距离量级的概念。</p>
<p>类似这种对比，我们还可以用在时间、空间、重量等概念上。随着孩子年龄的增长，量级的概念可以不断提升，比如帮助孩子理解人口的量级，理解国土面积的量级，理解宇宙空间的量级，理解光速、光年的概念等。</p>
