<p>推荐教具：七巧板</p>
<p>七巧板的原理就是图形的组合和分解，让孩子多练习七巧板，可以帮助他们构建出良好的空间感。适合年龄：</p>
<p>4~6岁，难度可以不断加大</p>
<h2><strong>平面图形的延伸与变换</strong></h2>
<p>推荐教具：拼图</p>
<p>拼图的过程中，孩子需要根据已有的图案去想象缺失的图案，这个过程就是在锻炼孩子图像延伸的能力。</p>
<p>适合年龄：3~6岁</p>
<h2><strong>立体图形的延伸与变换</strong></h2>
<p>推荐玩具：拼图索玛方块</p>
<p>索玛方块可以看是一种立体的拼图，孩子在玩的过程中，可以锻炼到空间立体的想象能力。</p>
<p>适合年龄：5岁以上</p>
