<p><strong>&ldquo;空间感&rdquo;能力并不是一种与生俱来的能力，而是一种需要在实际生活中基于很多体验、很多训练后逐渐形成的能力。</strong>在这些训练中，幼儿的&ldquo;图形认知启蒙&rdquo;便是一项最最基本的训练。在这里，可能有些朋友会发出疑问，幼儿园的小孩是否有能力进行&ldquo;图形认知&rdquo;的学习呢？答案是可以的。</p>
<p>在教育部发布的<strong>《3~6岁儿童学习与发展指南》</strong>中，就有明确规定了感知图形的内容。而且，3~6岁的儿童，抽象思维正在逐步建立的过程中，这个时候他们已经有了基本的思维能力去进行&ldquo;图形认知&rdquo;的学习了。事实上，图形认知已经成为很多学校幼升小的基本要求了。在小学数学课本里，图形认识也是一项重要的学习内容。</p>
