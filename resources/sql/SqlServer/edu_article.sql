<h2><strong>No.3 </strong>数学启蒙第二步：构建空间感</h2>
<p><strong>数和形，是数学的两大核心内容，它们就如同数学的两条腿，相辅相成，缺一不可。数感和空间感的培养，自然也是数学启蒙中最重要的两个任务。</strong></p>
<p>1<strong> 什么是"空间感"？</strong></p>
<p>在进入这个主题之前，我想先和大家分享一下自己大学期间经历的与空间感有关的故事。那是我第一次感受到，一个人的空间感是实实在在存在的，而且人与人之间的空间能力是真的有实质性差距的。高中时期我的数学成绩还算不错，考试基本都在140分以上，偶尔也可以接近满分。高中数学中几何占了很大一部分的，所以我自认为几何能力也算是不错的。</p>
<p>到了大一，我学的是工科，需要上一门课叫《机械制图》。在这门课中，我们需要学习投影法，还要学习用投影法画出零部件的三视图和六视图（如下图所示）。</p>
