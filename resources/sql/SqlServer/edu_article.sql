<p>4<strong> 如何在家庭中帮助孩子构建&ldquo;空间感&rdquo;？</strong></p>
<p>熟悉我的文章的朋友应该知道，我是极力推崇<strong>&ldquo;数学源于生活&rdquo;</strong>的理念。那么，如何才能把上述&ldquo;图形认知&rdquo;的四个方面融入到我们的日常生活中呢？下面我来逐一给大家分享。首先，我们需要引导孩子观察生活中物品的图形。比如车轮是圆形的、餐桌是长方形、奶粉罐是圆柱体之类的。</p>
<p>引导孩子观察生活中物品的形状，有一个好处就是让孩子先从不规则图形入手来认识图形。大家可能没有留意到，我们课本里的图形都是理想图形，比如正方形，长方形，三角形，这些图形是通过理想化处理的。在我们实际生活中的图形，大部分并不是这样标准的，比如一张桌子，整体上看是长方形，但每个角都是有处理的，倒圆的。<strong>从不规则图形认识到规则图形，是一个抽象思维建立的过程，也是孩子在头脑里建立图形特征的最好方式。</strong>除了用生活物品引导孩子去发现图形、总结图形，我们可以利用一些辅助工具（也可以称为教具），来帮助孩子进一步建立图形的概念。在这里，我从图形认知的4个方面，给大家介绍几个我自己在家里使用的教具，供大家参考。</p>
