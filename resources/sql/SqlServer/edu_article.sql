<p>3<strong> 构建&ldquo;空间感"需要从何入手？</strong></p>
<p>我们在前面有提到，&ldquo;图形认知&rdquo;是孩子构建空间感最基本的训练。所以，培养孩子的空间感，我们可以从图形认知开始入手。那么，图形认知究竟是指哪些内容呢？关于这个问题，我们教育部按发布的<strong>《3~6岁儿童学习与发展指南》</strong>中是有明确规定的。</p>
