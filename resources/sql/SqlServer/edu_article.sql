<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">运用实物教学：</span></span></span><span class="bjh-p">切蛋糕的时候，将1个蛋糕分成一半，变成2个蛋糕，2个再分成4个，长久下来，孩子会开始有&ldquo;分数&rdquo;的概念。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">分水果的时候，也可以让孩子跟着大声数数，建立对数字的概念，并开始了解&ldquo;均分&rdquo;的意思。</span></p>
</div>
