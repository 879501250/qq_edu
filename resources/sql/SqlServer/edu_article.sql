<p><strong>数的进制</strong></p>
<p>&ldquo;妈妈，为什么我们数到9之后就要变成10（1和0）？&rdquo;</p>
<p>如果孩子问你这样的问题，你会怎么回答？我问过Sui，她说大概会回答说，我也不知道哦，让我去查查有没有答案再来告诉你。</p>
<p>数的进制确实是很少有人会关注的知识点，至少在我查阅过的数学启蒙书籍中还没有专门讲过这个知识点的。<strong>但我觉得，想要帮助孩子建立好的数感，数的进制应该要纳入启蒙范畴。</strong></p>
<p>回到那个问题，为什么我们数到9之后就要变成1和0？一般的说法是，因为我们人只有10只手指，当我们数到10以上的数字时就不够用了，所以古人就发明了进制的方法。每次数到第10位就做个记号（往前进一位），然后继续从0开始数下去。</p>
<p>十进制是我们生活中最熟悉的进制方法。除了十进制，其实我们还有很多的进制方法。比如时钟就是12进制，月份是十二进制，我们古代的天干地支也是十二进制。</p>
<p>在计算机中，我们使用的是二进制，每数到2就进一位，如下图所示。我曾尝试给我五年级的侄女讲二进制，她可以很轻松就听懂，并能够完成十进制和二进制的转换练习。所以数的进制并不是一个很难的知识点，我看到很多小学生的奥数题其实就是用进制关系去改编的。</p>
