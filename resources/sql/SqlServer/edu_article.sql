<p><br />03 如何帮助孩子建立数感？</p>
<p>接下来，我将从数感启蒙的四个方面逐个介绍。</p>
<p><strong>数与实物的关系</strong></p>
<p>数学的基础是数字，数字的本质是一种符号，这种符号是从具体实物中逐渐抽象出来的，这可是我们的祖先经过漫长的思维进化后才掌握的一项高级技能。</p>
<p>远古时期，我们那些生活在原始森林中的祖先们，很长一段时间里是不懂得&ldquo;2只野兽&rdquo;和&ldquo;2块石头&rdquo;之间是可以有联系的。慢慢地，他们才发现原来2只野兽和2块石头之间可以用同一个符号&ldquo;2&rdquo;来表示，这在人的大脑中是要经历一个很大的思维跃迁的。</p>
<p>&ldquo;2只野兽&rdquo;和&ldquo;2块石头&rdquo;是具体实物，而数字&ldquo;2&rdquo;是一个抽象符号，<strong>从具体实物上升到抽象符号，这是我们要帮助孩子掌握的第一个数学技能。</strong></p>
<p>小孩的抽象思维能力和大脑发育程度息息相关。一般来说，2.5岁到3.5岁之间，孩子开始有能力来理解抽象符号，也就是说可以开始进行一些数感启蒙。不过，需要强调一下的是，不同孩子大脑发育的速度不同，思维能力相差3到5个月是很正常的事情，这对孩子以后智力成长的影响几乎为零，所以大家大可不必因此而焦虑。</p>
<p><strong>数感启蒙的第一步是学习数数。随着大脑的发育，孩子学习数数一般要经历&ldquo;唱数－点数－默数&rdquo;三个过程。</strong></p>
<figure data-size="normal"><noscript><img src="https://pic1.zhimg.com/50/v2-b0a3acff5601a7cecfae2324afdf1ca9_hd.jpg?source=1940ef5c" data-caption="" data-size="normal" data-rawwidth="803" data-rawheight="597" data-default-watermark-src="https://pic4.zhimg.com/50/v2-12ccee4985b6749c6e05711831cb5b71_hd.jpg?source=1940ef5c" class="origin_image zh-lightbox-thumb" width="803" data-original="https://pic2.zhimg.com/v2-b0a3acff5601a7cecfae2324afdf1ca9_r.jpg?source=1940ef5c"/></noscript><img class="origin_image zh-lightbox-thumb lazy" src="https://pic1.zhimg.com/80/v2-b0a3acff5601a7cecfae2324afdf1ca9_720w.jpg?source=1940ef5c" width="803" data-caption="" data-size="normal" data-rawwidth="803" data-rawheight="597" data-default-watermark-src="https://pic4.zhimg.com/50/v2-12ccee4985b6749c6e05711831cb5b71_hd.jpg?source=1940ef5c" data-original="https://pic2.zhimg.com/v2-b0a3acff5601a7cecfae2324afdf1ca9_r.jpg?source=1940ef5c" data-actualsrc="https://pic1.zhimg.com/50/v2-b0a3acff5601a7cecfae2324afdf1ca9_hd.jpg?source=1940ef5c" data-lazy-status="ok" /></figure>
<p>大多数孩子在1岁半左右就可以数1，2，3&hellip;&hellip;，这个时候很多家长会兴奋地认为宝宝已经学会数数了，甚至还有些家长会乐于在这个阶段&ldquo;炫耀&rdquo;自己的孩子已经可以数到20，数到30，甚至数到100。其实，这个阶段属于&ldquo;唱数&rdquo;阶段，唱数阶段孩子是不理解数与实物之间的关系的，数数对于宝宝来说就像是在唱一首好听的儿歌而已。</p>
<p>所以，这个阶段并不需要花太多精力去教孩子数数，这对后续的数学能力提升并没有多大的帮助。当然，如果孩子喜欢，多数一数也无妨。</p>
<p>经历了一段时间的唱数，孩子就会进入点数阶段。<strong>点数阶段是孩子进行数学启蒙的重要环节，因为这个阶段是孩子建立&ldquo;数与实物关系&rdquo;的关键时期。</strong></p>
<p>点数阶段一般发生在2岁半到3岁半之间。这个阶段，最好的启蒙方法就是让孩子点数实物。最常见的就是点数手指，边点手指边数1，2，3，4，5。除了手指，还可以引导孩子点数家里的椅子，桌子，玩具，水果等等。<strong>根据我自己的观察，这个阶段孩子会很乐意陪你来数，因为每次点完他都可以感受到满满的成就感。</strong></p>
<p>通过不断的重复练习，孩子一般在2~3个月内就可以熟练进行10以内的点数。这个时候，家长可以开始引导孩子学习默数。<strong>学习默数的过程是一个让数字与实物剥离的过程，这个时候孩子需要在头脑里建立抽象数字的概念。</strong>这属于思维层面的提升，对孩子来说是有一定难度的，需要一定的时间来进行练习。孩子学习默数大概会持续6~12个月，家长在引导孩子学习默数的时候要留有足够的耐心，不要太着急。</p>
<p>学习默数的方法是逐步引导孩子不动手指来数数。刚开始孩子是很难离开手指来数数的，遇到要数数就会不自觉地拿出手指。这个时候，我们可以通过玩游戏的方式来带领他学习默数。比如和孩子约定好，大家都不动手不出声，然后看谁数得快、数得准。类似这样游戏，大家可以任意发挥。</p>
<p><strong>如果孩子能够熟练掌握默数的能力，就说明孩子已经完全理解了抽象数字的概念，也能够理解了数与实物之间的关联关系。</strong>这个时候，数感启蒙的第一步&mdash;&mdash;数数就可以大功告成了！</p>
<p><strong>数的顺序</strong></p>
<p>会数数并不等于能够理解数的顺序，这是我从Shen身上观察出来的。</p>
<p>Shen3岁左右的时候，有一次我问他6的数字后面是什么？他答不上来。这让我有点吃惊，因为这个时候他已经很熟练地掌握了点数的技能，也基本掌握了10以内的默数，所以我原本默认了他已经懂得10以内的顺序关系。</p>
<p>后来我才知道，对于孩子来说，数的顺序关系也是需要一个练习过程的。这就好比我们刚开始学习英文数字一样，一直数下去我们可以数得很溜，但如果要单独说出某个数字就会比较吃力，如果还要按前后顺序来说，就更难了。</p>
<p><strong>所以，数感启蒙的第二步，是帮助孩子理解数的顺序。</strong>理解数的顺序方法也比较简单，只要多创造一些需要用到数的顺序的场景就可以了。比如吃水果的时候，可以每个人分一个号码，然后按顺序拿水果；也可以把数字贴在墙上，让孩子按一定的顺序数数，正数，反数，跳着数等；还可以多进行抽像数字的顺序对答练习，比如练习抢答&ldquo;4的后面是什么，8的前面是什么&rdquo;这样的游戏。</p>
