<p class="ztext-empty-paragraph">&nbsp;</p>
<p><strong>举个大家都能理解的例子。</strong>几乎全世界的幼儿园教数学，都会从1个橘子+2个橘子等于3个橘子开始，这叫<strong>具象</strong>。而学完橘子，老师就会教1+2=3。123都是数字符号，这叫<strong>抽象</strong>。而新加坡的数学，正是在橘子到数字符号之间，增加了一步<strong>形象</strong>化的图形符号。这个过程，就是<strong>建模的开始</strong>。</p>
<p class="ztext-empty-paragraph">&nbsp;</p>
<p>&nbsp;</p>
<figure data-size="normal"><img class="content_image lazy" src="https://pic4.zhimg.com/80/v2-c04fbdfe98dc38f9a47eeff4af88dd33_720w.jpg" width="407" data-rawwidth="407" data-caption="" data-size="normal" data-rawheight="215" data-actualsrc="https://pic4.zhimg.com/v2-c04fbdfe98dc38f9a47eeff4af88dd33_b.jpg" data-lazy-status="ok" /></figure>
<p>&nbsp;</p>
<p class="ztext-empty-paragraph">&nbsp;</p>
<p>这种建模的教学法叫<strong>CPA教学法</strong>，在新加坡的幼儿园和小学的数学中用得非常广泛。</p>
<p>&nbsp;</p>
<figure data-size="normal"><img class="origin_image zh-lightbox-thumb lazy" src="https://pic4.zhimg.com/80/v2-85cb8aac35295bae5b114990e04b5443_720w.jpg" width="639" data-rawwidth="639" data-caption="" data-size="normal" data-rawheight="137" data-original="https://pic4.zhimg.com/v2-85cb8aac35295bae5b114990e04b5443_r.jpg" data-actualsrc="https://pic4.zhimg.com/v2-85cb8aac35295bae5b114990e04b5443_b.jpg" data-lazy-status="ok" /></figure>
