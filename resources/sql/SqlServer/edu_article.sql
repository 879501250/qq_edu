<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">在生活中引导孩子学习数学</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">早期数学教育不光只是教数数，而是要引导孩子在生活中运用数学，让数学变得真实而有意义。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">那么，怎么在生活中建立孩子的数学概念呢？</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">把握机会教育：</span></span></span><span class="bjh-p">一般来说，5岁的孩子就能开始了解用钱购物的观念。</span></p>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">去超市购物时，家长可以让孩子自已选择1-2件物品去结帐，在交易的过程中，不但有交换、集合、分散的练习，更可以从经验上累积对数字概念的了解。</span></p>
</div>
<div class="index-module_mediaWrap_213jB">
<div class="index-module_contentImg_JmmC0"><img class="index-module_large_1mscr" src="https://pics0.baidu.com/feed/b90e7bec54e736d10ae76d1eb69814c7d4626979.jpeg?token=524ea7412ec476f215d2c78e3ceb9c07&amp;s=FC9EFFBE6CD2F1F3800D88E00300E02B" width="640" /></div>
</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">米粒爸去超市经常精打细算，精确到一根葱、一头蒜都不会错。</span></p>
