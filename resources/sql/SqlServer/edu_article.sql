</div>
<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-p">米粒在老爸的耳濡目染下，也爱跟我们一块做计划。他想吃什么水果零食、要花多少钱，事先要跟老爸申报：</span></p>
