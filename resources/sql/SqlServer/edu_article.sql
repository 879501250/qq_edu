<div class="index-module_textWrap_3ygOc">
<p><span class="bjh-ul"><span class="bjh-li"><span class="bjh-p">训练孩子使用正确的名称及单位：</span></span></span><span class="bjh-p">一双鞋、一盘水果、一支铅笔、一只小狗、一首歌曲、一张纸，生活中，家长尽量让孩子用正确的单位来形容物品，从日常生活里来提升孩子对正确数量及大小的描述及应用。</span></p>
